public class For {

    public static void main(String[] args) {
        for (int i = 9; i >= 1; i--) { // Recorremos los números del 9 al 1 en orden descendente
            System.out.print(i + " "); // Imprimimos cada número seguido de un espacio en la misma línea
        }
    }
}
