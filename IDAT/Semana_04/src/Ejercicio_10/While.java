public class While {

    public static void main(String[] args) {
        int i = 9; // Inicializamos la variable 'i' en 9
        while (i >= 1) { // Mientras 'i' sea mayor o igual a 1
            System.out.print(i + " "); // Imprimimos el valor actual de 'i' seguido de un espacio en la misma línea
            i--; // Decrementamos 'i' en 1 en cada iteración
        }
    }
}
